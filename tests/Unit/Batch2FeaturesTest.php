    $reflection = new ReflectionClass(HasAuditLog::class);
    $methods = array_map(fn ($m) => $m->getName(), $reflection->getMethods());

    expect($methods)->toContain('tableAuditLogName');
    expect($methods)->toContain('tableAuditLogTable');
    expect($methods)->toContain('tableAuditLogEnabled');
    expect($methods)->toContain('logInlineEdit');
