// ── GenerateTranslations command ──────────────────────────

test('GenerateTranslations command class exists', function () {
    expect(class_exists(GenerateTranslations::class))->toBeTrue();
});

test('GenerateTranslations has correct signature', function () {
    $reflection = new ReflectionClass(GenerateTranslations::class);
    $prop = $reflection->getProperty('signature');
    $prop->setAccessible(true);

    $command = $reflection->newInstanceWithoutConstructor();
    $signature = $prop->getValue($command);

    expect($signature)->toContain('data-table:translations');
    expect($signature)->toContain('--lang');
    expect($signature)->toContain('--all');
    expect($signature)->toContain('--output');
});

test('GenerateTranslations has built-in language packs', function () {
    $reflection = new ReflectionClass(GenerateTranslations::class);
    $prop = $reflection->getProperty('translations');
    $prop->setAccessible(true);

    $command = $reflection->newInstanceWithoutConstructor();
    $translations = $prop->getValue($command);

    $expectedLangs = ['en', 'fr', 'es', 'de', 'pt', 'ar', 'zh', 'ja'];

    foreach ($expectedLangs as $lang) {
        expect($translations)->toHaveKey($lang);
    }
});

test('GenerateTranslations each language has required keys', function () {
    $reflection = new ReflectionClass(GenerateTranslations::class);
    $prop = $reflection->getProperty('translations');
    $prop->setAccessible(true);

    $command = $reflection->newInstanceWithoutConstructor();
    $translations = $prop->getValue($command);

    $requiredKeys = [
        'totalResults', 'rowsPerPage', 'pageOf', 'columns', 'filter', 'search',
        'yes', 'no', 'selectAll', 'cancel', 'save', 'noData', 'actions', 'loading',
        'density', 'undo', 'redo', 'keyboardShortcuts', 'close', 'emptyTitle',
    ];

    foreach ($translations as $lang => $strings) {
        foreach ($requiredKeys as $key) {
            expect(array_key_exists($key, $strings))->toBeTrue("Language '{$lang}' is missing key '{$key}'");
        }
    }
});

test('GenerateTranslations buildTypeScript generates valid content', function () {
    $reflection = new ReflectionClass(GenerateTranslations::class);
    $method = $reflection->getMethod('buildTypeScript');
    $method->setAccessible(true);

    $command = $reflection->newInstanceWithoutConstructor();
    $result = $method->invoke($command, 'es', [
        'columns' => 'Columnas',
        'filter' => 'Filtrar',
        'totalResults' => '(count) => `${count} resultado${count !== 1 ? "s" : ""}`',
    ]);

    expect($result)->toContain('esTranslations');
    expect($result)->toContain("'Columnas'");
    expect($result)->toContain("'Filtrar'");
    expect($result)->toContain('(count) =>');
    expect($result)->toContain('Generated by');
});

