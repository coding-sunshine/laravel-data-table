    expect($methods)->toContain('getRowAuditLog');
});

test('HasAuditLog tableAuditLogName is abstract', function () {
    $reflection = new ReflectionClass(HasAuditLog::class);
    $method = $reflection->getMethod('tableAuditLogName');

    expect($method->isAbstract())->toBeTrue();
});

test('HasAuditLog default table name is data_table_audit_log', function () {
    $class = new class {
        use HasAuditLog;

        public static function tableAuditLogName(): string
        {
            return 'test-table';
        }
    };

    expect($class::tableAuditLogTable())->toBe('data_table_audit_log');
});

test('HasAuditLog default enabled is true', function () {
    $class = new class {
        use HasAuditLog;

        public static function tableAuditLogName(): string
        {
            return 'test-table';
        }
    };

    expect($class::tableAuditLogEnabled())->toBeTrue();
});

test('HasAuditLog enabled can be overridden to false', function () {
    $class = new class {
        use HasAuditLog;

        public static function tableAuditLogName(): string
        {
            return 'test-table';
        }

        public static function tableAuditLogEnabled(): bool
        {
            return false;
        }
    };

    expect($class::tableAuditLogEnabled())->toBeFalse();
});

